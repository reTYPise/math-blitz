import assert from 'node:assert/strict';
import { readFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

const root = dirname(dirname(fileURLToPath(import.meta.url)));
const html = readFileSync(join(root, 'index.html'), 'utf8');

assert.match(html, /id="range-select"/, 'range should be controlled by a select');
assert.match(html, /id="focus-select"/, 'fixed operand should be controlled by a select');
assert.match(html, /id="guide-toggle"/, 'answer guide toggle should be available');
assert.match(html, /id="sequence-mode-select"/, 'question sequence mode should be controlled by a select');
assert.match(html, /<option value="random" selected>всегда случайно<\/option>/, 'random order should be the default option');
assert.match(html, /<option value="ordered-first">сначала по порядку, потом случайно<\/option>/, 'ordered-first mode should be selectable');
assert.match(html, /<option value="ordered">всегда по порядку<\/option>/, 'always-ordered mode should be selectable');
assert.match(html, /id="guide-reveal-btn"/, 'collapsed guide should have a reveal button');
assert.match(html, /id="practice-guide"/, 'game screen should contain an answer guide panel');
assert.match(html, /class="game-shell"/, 'game screen should use a responsive shell');
assert.match(html, /<title>Тренажер устного счета math-blitz/, 'SEO title should describe the tool');
assert.match(html, /<meta name="description"/, 'SEO description meta tag should be present');
assert.match(html, /<link rel="canonical"/, 'canonical link should be present');
assert.match(html, /<h1 class="logo">math<span>-<\/span>blitz<\/h1>/, 'visible h1 should be present');
assert.match(html, /class="seo-description"/, 'long SEO description should be rendered under the start button');
assert.match(html, /function updateFocusOptions\(/, 'focus options should react to range changes');
assert.match(html, /function syncSettingsFromControls\(/, 'start should trust visible control values');
assert.match(html, /function syncControlsFromState\(/, 'menu controls should reflect current state after navigation');
assert.match(html, /syncSettingsFromControls\(\);\s*lastSettings/, 'settings should be synced before a game starts');
assert.match(html, /function renderPracticeGuide\(/, 'guide should render answer rows');
assert.match(html, /function setGuideExpanded\(/, 'guide should be expandable during the game');
assert.match(html, /function highlightGuideAnswer\(/, 'guide should highlight the current answer row');
assert.match(html, /function getOperandPair\(/, 'question generator should support a fixed operand');
assert.match(html, /currentQuestion\s*=/, 'current question should be tracked for contextual guide rows');
assert.match(html, /function getGuideAnchor\(/, 'guide should choose an anchor from the current question');
assert.match(html, /function buildContextGuideRows\(/, 'any-number mode should render a contextual guide instead of the full table');
assert.match(html, /function buildOrderedQueue\(/, 'ordered-first mode should build a deterministic first pass');
assert.match(html, /function getNextQuestion\(/, 'question selection should go through repeat-safe sequencing');
assert.match(html, /function isSameQuestion\(/, 'random sequencing should avoid immediate repeated questions');
assert.match(html, /function selectSequenceMode\(/, 'sequence mode select should update ordering behavior');
assert.match(html, /function handleFastGameButtonPress\(/, 'game answer buttons should react on immediate pointer press');
assert.match(html, /addEventListener\('pointerdown', handleFastGameButtonPress\)/, 'game answer buttons should use pointerdown for speed');
assert.match(html, /if \(event\.button !== 0\) return;/, 'fast press should only accept the left mouse button');
assert.doesNotMatch(html, /class="choice-btn"[^>]*onclick=|class="sk-btn[^"]*"[^>]*onclick=/, 'game answer buttons should not wait for cancellable click');
assert.doesNotMatch(html, /ui-scale|uiScale|setUiScale|scale-row|ui-scale-slider/, 'UI scale feature should be fully removed');
assert.match(html, /\.guide-row\.current/, 'guide rows should have a current-answer highlight state');
assert.match(html, /grid-template-columns:\s*minmax\(0,\s*520px\)\s+minmax\(260px,\s*1fr\)/, 'desktop game layout should use the free horizontal space');
